function res = g2(z, q, p1, p2, DIGITS)
%--------------------------------------------------------------------------
digits(DIGITS);
%--------------------------------------------------------------------------
temp = vpa(1);
%--------------------------------------------------------------------------
for i = 1 : 1 : q
    temp = vpa(vpa(temp) * (vpa(z) - vpa(p1)));
end
%--------------------------------------------------------------------------
res = vpa(vpa(numerator(vpa(z), vpa(p2), DIGITS)) / vpa(temp));
%--------------------------------------------------------------------------
end

